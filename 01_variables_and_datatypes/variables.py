"""
variables.py
Basic examples of variables in Python
"""

# Variable declaration
name = "Mansi"
age = 20
height = 5.3
is_student = True

# Printing variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Updating a variable
age = 21
print("Updated Age:", age)
