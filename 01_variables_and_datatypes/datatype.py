"""
data_types.py
Examples of basic data types in Python
"""

# Integer
number = 10
print("Number:", number, "| Type:", type(number))

# Float
price = 99.50
print("Price:", price, "| Type:", type(price))

# String
course = "Python Programming"
print("Course:", course, "| Type:", type(course))

# Boolean
is_python_easy = True
print("Is Python Easy:", is_python_easy, "| Type:", type(is_python_easy))
